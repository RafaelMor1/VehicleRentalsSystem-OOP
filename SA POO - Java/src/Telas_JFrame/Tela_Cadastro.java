    private void Cadastro_RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Cadastro_RegistrarActionPerformed
        try {
    
        // Obter os valores dos campos de entrada
    String nome = Cadastro_Nome.getText();
    int cnh = Integer.parseInt(Cadastrar_CNH.getText());
    String categoria = Cadastrar_Categoria.getSelectedItem().toString();
    int idade = Integer.parseInt(Cadastro_Idade.getText());
    String email = Cadastrar_Email.getText();
    String senha = new String(Cadastrar_Senha.getPassword());

    // Criar uma nova inst칙ncia de Usuario com os valores obtidos
    Usuario novoUsuario = new Usuario();
    novoUsuario.setNome(nome);
    novoUsuario.setCnh(cnh);
    novoUsuario.setCategoria(categoria);
    novoUsuario.setIdade(idade);
    novoUsuario.setEmail(email);
    novoUsuario.setSenha(senha);
    
    Tela_Agendamento ta = new Tela_Agendamento();
    
    ta.labelNome.setText(nome);
    ta.labelEmail.setText(email);
    ta.dispose();
    

    usuarios.add(novoUsuario);
    
    if(idade < 18){
        JOptionPane.showMessageDialog(null, "Idade inferior a 18 anos!!", "ERROR", JOptionPane.ERROR_MESSAGE);
    }
    else{
        JOptionPane.showMessageDialog(null, "Usu치rio cadastrado com sucesso!");

    Tela_Login tl = new Tela_Login(usuarios);
    tl.setVisible(true);
    this.dispose(); 
    }
    
    
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, "PREENCHA TODOS OS CAMPOS!!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        
    
    
    
   
    }//GEN-LAST:event_Cadastro_RegistrarActionPerformed

